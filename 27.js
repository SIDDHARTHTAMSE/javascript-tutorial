// array push pop

// array shift unshift


// push --> it will add the element from last index
let fruits = ["Apple", "Chikku", "Banana"];
console.log(fruits);
fruits.push("Orange");
console.log(fruits);

// pop --> it will remove the element from last index
let fruits1 = ["Apple", "Chikku", "Banana"];
console.log(fruits1);
fruits1.pop();
console.log(fruits1);

// unshift --> it will add the element from first index
let fruit2 = ["Apple", "Chikku", "Banana"];
fruit2.unshift("Graphes");
console.log(fruit2);

// shift  --> it will remove the element from first index
let fruit3 = ["Apple", "Chikku", "Banana"];
console.log(fruit3);
fruit3.shift("Chikku");
console.log(fruit3);
